package main

import "fmt"

func main()  {

	fmt.Println("\nTipe Data Number")
	// Tipe data Number
	fmt.Println("Satu", 1)
	fmt.Println("Dua", 2)
	fmt.Println("Tiga koma Lima", 3.5)

	fmt.Println("\nTipe Data Boolean")
	// Tipe data Boolean
	fmt.Println("Benar = ", true)
	fmt.Println("Salah = ", false)

	fmt.Println("\nTipe Data String")
	// Tipe data String
	fmt.Println("Alfredo")
	fmt.Println("Alfredo Dwi")
	fmt.Println("Alfredo Dwi Putra")


	fmt.Println(len("Alfredo"))
	fmt.Println("Alfredo Dwi"[0])
	fmt.Println("Alfredo Dwi Putra"[1])
	
}